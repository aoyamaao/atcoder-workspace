#include <bits/stdc++.h>
using namespace std;

int main() {
  // 一年の秒数
  int seconds = 365 * 24 * 60 * 60;
  int year = seconds;
  int two_year = seconds * 2;
  int five_year = seconds * 5;
  int ten_year = seconds * 10;

  // 以下のコメント/* */を消して追記する
  cout << year << endl;
  cout << two_year << endl;
  cout << five_year << endl;
  cout << ten_year << endl;
}
